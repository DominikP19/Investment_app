CREATE TABLE IF NOT EXISTS TRANSACTION (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date DATE NOT NULL,
    description TEXT NOT NULL,
    transaction_type_id INTEGER NOT NULL,
    asset_id INTEGER NOT NULL,
    quantity INTEGER NOT NULL,
    currency VARCHAR(3) NOT NULL,
    price NUMERIC(20,4) NOT NULL,
    total_amount NUMERIC(20,2) NOT NULL DEFAULT 0,
    fee NUMERIC(20,2) NOT NULL DEFAULT 0,
    tax_amount NUMERIC(20,2) NOT NULL DEFAULT 0,
    portfolio_id INTEGER NOT NULL,
    constraint fk_portfolio FOREIGN KEY (portfolio_id) REFERENCES PORTFOLIO(id) ON DELETE CASCADE,
    constraint fk_asset FOREIGN KEY (asset_id) REFERENCES ASSET(id) ON DELETE CASCADE,
    constraint fk_transaction_type FOREIGN KEY (transaction_type_id) REFERENCES TRANSACTION_TYPE(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS BOOKING (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date DATE NOT NULL,
    transaction_id INTEGER NOT NULL,
    debit_account_id INTEGER NOT NULL,
    credit_account_id INTEGER NOT NULL,
    currency VARCHAR(3) NOT NULL,
    amount NUMERIC(20,2) NOT NULL,
    constraint fk_transaction FOREIGN KEY (transaction_id) REFERENCES TRANSACTION(id) ON DELETE CASCADE,
    constraint fk_debit_account FOREIGN KEY (debit_account_id) REFERENCES ACCOUNT(id) ON DELETE CASCADE,
    constraint fk_credit_account FOREIGN KEY (credit_account_id) REFERENCES ACCOUNT(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS PORTFOLIO_VALUATION (
