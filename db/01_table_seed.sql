CREATE TABLE IF NOT EXISTS POSITION (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    portfolio_id INTEGER NOT NULL,
    asset_id INTEGER NOT NULL,
    currency VARCHAR(3) NOT NULL,
    quantity INTEGER NOT NULL,
    average_price NUMERIC(20,4) NOT NULL,
    fee NUMERIC(20,4),
    total_amount NUMERIC(20,4),
    constraint fk_portfolio FOREIGN KEY (portfolio_id) REFERENCES PORTFOLIO(id) ON DELETE CASCADE,
    constraint fk_asset FOREIGN KEY (asset_id) REFERENCES ASSET(id) ON DELETE CASCADE,
    constraint unique_position UNIQUE (portfolio_id, asset_id)
);

CREATE TABLE IF NOT EXISTS TAX_LOT (
